
package com.mycompany.practica_8_encapsulamiento;


public class Practica_8_Encapsulamiento {

    public static void main(String[] args) {
        System.out.println("Hello World!");
    }
}
/*

    * Los atributos son las variables que contienen nuestra clase
    * Los metodos son las funciones que va a realizar nuestra clase.

Encapsulamiento 

en java consiste en controlar el acceso a los datos, que conforman un objeto o 
instancia de una clase es decir, indicar que metodos y atributos sin publicos,

para poder revisar contenido e incluso ser modificados y a su vez que metodos y atributos
son privados, para evitar el acceso a su contenido o que se realice alguna modificacion en ellos.


*/